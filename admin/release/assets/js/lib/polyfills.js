/**
 * Compares all of the elements of this array is present in array given by {array} parameter
 * @param {Array} array
 * @returns {boolean}
 */
Array.prototype.equal = function(array)
{
    if (this === null) {
      throw new TypeError('Array.prototype.compare called on null or undefined');
    }
    if (!Array.isArray(array)) {
      throw new TypeError('array parameter must be an Array');
    }
    
    if (array.length !== this.length) return false;
    
    return this.every(function(i) {return array.contains(i);});
};

